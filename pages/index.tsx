import type { NextPage } from "next";
import Head from "next/head";
import Image from "next/image";

import styles from "../styles/Home.module.css";
import { useEffect } from "react";
import { useDispatch, useSelector } from "react-redux";
import { AppState, todoType } from "../types/actions";
import { ADD_TODO, fetchAllToDo } from "../store/actions";

const Home: NextPage = () => {
  const dispatch = useDispatch();
  useEffect(() => {
    dispatch(fetchAllToDo());
  }, [dispatch]);

  const { todoItems } = useSelector<AppState, AppState["todo"]>(
    ({ todo }) => todo
  );
  return (
    <div className={styles.container}>
      <Head>
        <title>Practice Redux</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        {todoItems && (
          <ul>
            {Object.entries(todoItems).map(([i, item]) => {
              return <li key={i}>{item.title}</li>;
            })}
          </ul>
        )}
      </main>
    </div>
  );
};

export default Home;
